part of 'authentication_bloc.dart';

abstract class AuthenticationEvent extends Equatable {
  const AuthenticationEvent();

  @override
  List<Object> get props => [];
}

/// Representa cuando la autenticacion cambia
class AuthenticationChange extends AuthenticationEvent {
  final AuthenticationStatus status;

  const AuthenticationChange({this.status = AuthenticationStatus.unknown});

  @override
  List<Object> get props => [status];

  @override
  String toString() => 'AuthenticationOnLoading { status : $status }';
}

class AuthenticationLogoutRequested extends AuthenticationEvent {}
